// Helper to get all cities as an array
export const getAllServiceAreas = () => Object.entries(SERVICE_AREAS).map(([slug, data]) => ({
  slug,
  ...data,
}));

// Get primary service areas only
export const getPrimaryServiceAreas = () => 
  getAllServiceAreas().filter(area => area.tier === 'primary');

// Get all areas for a specific tier
export const getServiceAreasByTier = (tier) => 
  getAllServiceAreas().filter(area => area.tier === tier);

// Get service area by slug
export const getServiceArea = (slug) => SERVICE_AREAS[slug] || null;

// Get nearby areas as full objects
export const getNearbyAreas = (slug) => {
  const area = SERVICE_AREAS[slug];
  if (!area) return [];
  return area.nearbyAreas
    .map(name => {
      const nearbySlug = name.toLowerCase().replace(/\s+/g, '-').replace(/['.]/g, '');
      return SERVICE_AREAS[nearbySlug] ? { slug: nearbySlug, ...SERVICE_AREAS[nearbySlug] } : null;
    })
    .filter(Boolean);
};

// Generate sitemap data
export const getSitemapData = () => 
  getAllServiceAreas().map(area => ({
    url: `/${area.slug}`,
    name: area.name,
    priority: area.tier === 'primary' ? 1.0 : area.tier === 'secondary' ? 0.8 : 0.6,
  }));

// Export simple list for navigation/footer
export const SERVICE_AREA_LIST = getAllServiceAreas().map(area => ({
  name: area.name,
  slug: area.slug,
  tier: area.tier,
}));
